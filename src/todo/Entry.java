package todo;

import java.util.Calendar;

/**
 *
 * @author hazem
 */
public class Entry {
    private String name;
    private String date;
    
    public Entry(String name, String date){
        this.name = name;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    
    @Override
    public String toString(){
        return (name + " "  + date);
    }
    
}
